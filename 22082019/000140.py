b = [20, 10, 5]

#imprimir cada elemento de la lista
for e in b:
    print (e)
    
#sumar valores d ela lista
Total = 0 #variable que representa la suma acumulada 
for e in b: #para cada elemento de la lista se ejecuta la siguiente instrucción:
    Total += e #cada elemento de la lista es sumado a variable "total"

print (Total) #se imprime suma acumulada final


    